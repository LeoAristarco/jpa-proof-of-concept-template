
    create table Cancha (
        CANCHA_ID bigint generated by default as identity (start with 1),
        nombre varchar(255),
        tieneIluminacion boolean,
        color_id bigint,
        primary key (CANCHA_ID)
    )

    create table Cancha_Reserva (
        Cancha_CANCHA_ID bigint not null,
        reservas_reservaId bigint not null
    )

    create table Color (
        id bigint generated by default as identity (start with 1),
        descripcion varchar(255),
        primary key (id)
    )

    create table Fabricante (
        idFabricante bigint generated by default as identity (start with 1),
        nombreFabricante varchar(255),
        primary key (idFabricante)
    )

    create table Inscripcion (
        inscripcionId bigint generated by default as identity (start with 1),
        jugador_jugadorId bigint,
        paleta_paletaId bigint,
        primary key (inscripcionId)
    )

    create table Jugador (
        jugadorId bigint generated by default as identity (start with 1),
        apellido varchar(255),
        domicilio varchar(255),
        fechaNacimiento timestamp,
        nombre varchar(255),
        telefono varchar(255),
        paleta_paletaId bigint,
        primary key (jugadorId)
    )

    create table Paleta (
        paletaId bigint generated by default as identity (start with 1),
        grosor integer,
        nombre varchar(255),
        color_id bigint,
        primary key (paletaId)
    )

    create table Reserva (
        reservaId bigint generated by default as identity (start with 1),
        fechaHoraFin timestamp,
        fechaHoraInicio timestamp,
        colorCancha_id bigint,
        primary key (reservaId)
    )

    create table Reserva_Inscripcion (
        Reserva_reservaId bigint not null,
        inscripciones_inscripcionId bigint not null
    )

